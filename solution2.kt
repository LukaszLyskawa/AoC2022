//A, X - rock, 1 point
//B, Y - paper, 2 points
//C, Z - scissors, 3 points
// loss - 0
// draw - 3
// win - 6
// 
// A Y - 2 + 6
// B X - 1 + 0
// C Z - 3 + 3
//
//Goal: get max

enum class Outcome(val value:Int){
    WIN(6), LOSS(0), DRAW(3)
}

enum class Symbol(val value:Int){
    ROCK(1),
    PAPER(2),
    SCISSORS(3)
}
    
fun playMatch(opponent:Symbol, you:Symbol): Int = 
    you.fightOutcome(opponent).value + you.value
    
fun Symbol.fightOutcome(other:Symbol): Outcome =
    when(this){
        Symbol.ROCK -> when(other){
            Symbol.ROCK -> Outcome.DRAW
            Symbol.PAPER -> Outcome.LOSS
            Symbol.SCISSORS -> Outcome.WIN
        }
        Symbol.PAPER ->when(other){
            Symbol.ROCK -> Outcome.WIN
            Symbol.PAPER -> Outcome.DRAW
            Symbol.SCISSORS -> Outcome.LOSS
        }
        Symbol.SCISSORS -> when(other){
            Symbol.ROCK -> Outcome.LOSS
            Symbol.PAPER -> Outcome.WIN
            Symbol.SCISSORS -> Outcome.DRAW
        }
    }
   
fun main() {
    val strategy = readInput(input.split("\n"))
    val matches = strategy.map{(opponent, you)->
        playMatch(opponent, you)
    }
    println(matches.sum())
}

